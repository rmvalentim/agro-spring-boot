package com.rafaelvalentim.agro.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.OneToMany;
import org.hibernate.annotations.GenericGenerator;

/**
 *
 * @author Rafael.Valentim
 */

@Entity
public class Equipamento implements Serializable {
    
    private static final long serialVersionUID = 1L;
    
    @Id
    @GeneratedValue(generator="increment")
    @GenericGenerator(name="increment",strategy="increment")
    private Long id;
    
    private String descricao;
    private String marca;
    private String modelo;
    
    @ManyToOne
    private GrupoEquipamento grupoEquipamento;
    
    @OneToMany
    @JoinColumn(name = "custo_equipamento_id")
    private List<CustoEquipamento> custosEquipamentos = new ArrayList<>();    
    

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the modelo
     */
    public String getModelo() {
        return modelo;
    }

    /**
     * @param modelo the modelo to set
     */
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    /**
     * @return the grupoEquipamento
     */
    public GrupoEquipamento getGrupoEquipamento() {
        return grupoEquipamento;
    }

    /**
     * @param grupoEquipamento the grupoEquipamento to set
     */
    public void setGrupoEquipamento(GrupoEquipamento grupoEquipamento) {
        this.grupoEquipamento = grupoEquipamento;
    }
    
    
}
